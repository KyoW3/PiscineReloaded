int	ft_iterative_factorial(int nb)
{
	int x;
	int y;

	x = 1;
	y = 1;
	if (nb == 0)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	while (y <= nb)
	{
		x = x * y;
		y++;
	}
	return (x);
}
